void Task_FIR_Filter(void *argument)
{
	/* --- FIR settings --- */
	    #define NUM_TAPS 15
	    #define BLOCK_SIZE 1   // 每次只處理一筆資料

	    /* 15 taps 移動平均濾波器 */
	    static const float32_t firCoeffs32[NUM_TAPS] = {
	        1.0f/15, 1.0f/15, 1.0f/15, 1.0f/15, 1.0f/15,
	        1.0f/15, 1.0f/15, 1.0f/15, 1.0f/15, 1.0f/15,
	        1.0f/15, 1.0f/15, 1.0f/15, 1.0f/15, 1.0f/15
	    };

	    /* --- 為每個軸建立獨立 FIR instance 和 state 緩衝區 --- */
	    arm_fir_instance_f32 Sx, Sy, Sz;

	    static float32_t state_x[NUM_TAPS + BLOCK_SIZE - 1];
	    static float32_t state_y[NUM_TAPS + BLOCK_SIZE - 1];
	    static float32_t state_z[NUM_TAPS + BLOCK_SIZE - 1];

	    arm_fir_init_f32(&Sx, NUM_TAPS, firCoeffs32, state_x, BLOCK_SIZE);
	    arm_fir_init_f32(&Sy, NUM_TAPS, firCoeffs32, state_y, BLOCK_SIZE);
	    arm_fir_init_f32(&Sz, NUM_TAPS, firCoeffs32, state_z, BLOCK_SIZE);

	    float32_t x_in, y_in, z_in;
	    float32_t x_out, y_out, z_out;

	    accelData_t raw;

	    for(;;)
	    {
	        /* 從 queue 拿到原始資料 */
	        if (osMessageQueueGet(accelQueueHandle, &raw, NULL, osWaitForever) == osOK)
	        {
	            x_in = raw.data[0] / 1000.0f;
	            y_in = raw.data[1] / 1000.0f;
	            z_in = raw.data[2] / 1000.0f;

	            /* 各軸獨立 FIR */
	            arm_fir_f32(&Sx, &x_in, &x_out, 1);
	            arm_fir_f32(&Sy, &y_in, &y_out, 1);
	            arm_fir_f32(&Sz, &z_in, &z_out, 1);

	            BLE_filtered_x = x_out;
	            BLE_filtered_y = y_out;
	            BLE_filtered_z = z_out;
	            BLE_filtered_new = 1;

	        }
	    }
}

